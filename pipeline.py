def merge_patches(patch_list, output_filename, file_type):
    """Merge multiple patches into a single patch."""
    if not patch_list or len(patch_list) < 2:
        return None
        
    try:
        # Determine output extension based on file type
        if file_type == 'india':
            output_path = os.path.join(PATCH_DIR, f"{output_filename}.mbtiles")
        elif file_type == 'poi':
            output_path = os.path.join(PATCH_DIR, f"{output_filename}.db") 
        elif file_type == 'navigator':
            output_path = os.path.join(PATCH_DIR, f"{output_filename}.tar")
        else:
            return None
            
        if file_type == 'india':
            success = merge_mbtiles_patches(patch_list, output_path)
        elif file_type == 'poi':
            success = merge_sqlite_patches(patch_list, output_path)
        elif file_type == 'navigator':
            success = merge_tar_patches(patch_list, output_path)
        else:
            return None
            
        if success:
            # Compress the merged patch
            compressed = compress_file_with_lz4(output_path, keep_original=False)
            if compressed:
                save_single_file_md5(compressed)
                return compressed
                
    except Exception as e:
        log(f"Error merging patches: {e}", "ERROR")
        
    return None

def merge_mbtiles_patches(patch_list, output_path):
    """Merge multiple MBTiles patch files."""
    import sqlite3
    import tempfile
    
    try:
        # Create output database
        with sqlite3.connect(output_path) as output_conn:
            output_cursor = output_conn.cursor()
            
            # Create tables structure (copy from first patch)
            first_patch_path = os.path.join(PATCH_DIR, patch_list[0])
            if first_patch_path.endswith('.lz4'):
                # Decompress temporarily
                with tempfile.NamedTemporaryFile(suffix='.mbtiles') as temp_file:
                    decompress_lz4_file(first_patch_path, temp_file.name)
                    copy_mbtiles_structure(temp_file.name, output_path)
            
            # Clear existing tiles
            output_cursor.execute("DELETE FROM tiles")
            
            # Merge all patches
            all_tiles = {}  # zoom/col/row -> tile_data (latest wins)
            
            for patch_file in reversed(patch_list):  # Process oldest first
                patch_path = os.path.join(PATCH_DIR, patch_file)
                
                with tempfile.NamedTemporaryFile(suffix='.mbtiles') as temp_file:
                    if patch_path.endswith('.lz4'):
                        decompress_lz4_file(patch_path, temp_file.name)
                        source_path = temp_file.name
                    else:
                        source_path = patch_path
                        
                    with sqlite3.connect(source_path) as patch_conn:
                        patch_cursor = patch_conn.cursor()
                        patch_cursor.execute("SELECT zoom_level, tile_column, tile_row, tile_data FROM tiles")
                        
                        for zoom, col, row, data in patch_cursor.fetchall():
                            tile_key = f"{zoom}/{col}/{row}"
                            all_tiles[tile_key] = (zoom, col, row, data)
            
            # Insert all merged tiles
            for tile_key, (zoom, col, row, data) in all_tiles.items():
                output_cursor.execute(
                    "INSERT OR REPLACE INTO tiles (zoom_level, tile_column, tile_row, tile_data) VALUES (?, ?, ?, ?)",
                    (zoom, col, row, data)
                )
            
            # Update metadata
            output_cursor.execute("INSERT OR REPLACE INTO metadata (name, value) VALUES (?, ?)", 
                                ("type", "merged_patch"))
            output_cursor.execute("INSERT OR REPLACE INTO metadata (name, value) VALUES (?, ?)", 
                                ("merged_patches", str(len(patch_list))))
            output_cursor.execute("INSERT OR REPLACE INTO metadata (name, value) VALUES (?, ?)", 
                                ("merge_timestamp", datetime.now().isoformat()))
            
            output_conn.commit()
            return True
            
    except Exception as e:
        log(f"Error merging MBTiles patches: {e}", "ERROR")
        return False

