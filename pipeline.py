def _south_for_xyz(z, x, xyz_y):
    """South edge latitude of a tile stored in XYZ convention (row 0 = north)."""
    n = 2 ** z
    return math.degrees(math.atan(math.sinh(math.pi * (1 - 2 * (xyz_y + 1) / n))))


def _south_for_tms(z, x, tms_y):
    """South edge latitude of a tile stored in TMS convention (row 0 = south)."""
    n     = 2 ** z
    xyz_y = (n - 1) - tms_y
    return math.degrees(math.atan(math.sinh(math.pi * (1 - 2 * (xyz_y + 1) / n))))


SQLITE_MAGIC = b"SQLite format 3\x00"

def _validate_mbtiles(path):
    """Return True if path is a non-empty valid SQLite file, False otherwise."""
    if not os.path.exists(path):
        return False
    if os.path.getsize(path) < 512:
        return False
    try:
        with open(path, 'rb') as f:
            header = f.read(16)
        return header == SQLITE_MAGIC
    except OSError:
        return False


def detect_tile_row_convention(source_path):
    """
    Auto-detect whether tile_row is stored as XYZ or TMS by sampling tiles
    and checking which interpretation places them inside India's bbox.
    Returns 'xyz' or 'tms'.
    """
    if not _validate_mbtiles(source_path):
        raise ValueError(f"Source MBTiles is missing or corrupt: {source_path}")
    conn = sqlite3.connect(f"file:{source_path}?mode=ro", uri=True)
    cur  = conn.cursor()
    cur.execute("SELECT zoom_level, tile_column, tile_row FROM tiles LIMIT 50")
    rows = cur.fetchall()
    conn.close()

    xyz_hits = 0
    tms_hits = 0
    for z, x, tr in rows:
        s_xyz = _south_for_xyz(z, x, tr)
        s_tms = _south_for_tms(z, x, tr)
        if INDIA_LAT_MIN <= s_xyz <= INDIA_LAT_MAX:
            xyz_hits += 1
        if INDIA_LAT_MIN <= s_tms <= INDIA_LAT_MAX:
            tms_hits += 1

    log(f"  Convention probe: xyz_hits={xyz_hits}, tms_hits={tms_hits} (out of {len(rows)} samples)")
    if tms_hits > xyz_hits:
        log("  → tile_row is TMS (row 0 = south) — will apply flip")
        return 'tms'
    else:
        log("  → tile_row is XYZ (row 0 = north) — no flip needed")
        return 'xyz'


def tile_bounds(z, x, tile_row, convention):
    """Return (west, south, east, north) geographic bounds for a tile."""
    n     = 2 ** z
    xyz_y = ((n - 1) - tile_row) if convention == 'tms' else tile_row
    west  = x       / n * 360.0 - 180.0
    east  = (x + 1) / n * 360.0 - 180.0
    north = math.degrees(math.atan(math.sinh(math.pi * (1 - 2 *  xyz_y      / n))))
    south = math.degrees(math.atan(math.sinh(math.pi * (1 - 2 * (xyz_y + 1) / n))))
    return west, south, east, north


def tile_to_grid_key(z, x, tile_row, convention):
    """
    Map a tile to its 1° SW-corner grid key.
    e.g. "12_80", "S3_72" (negative lat), "12_W5" (negative lon).
    """
    west, south, _e, _n = tile_bounds(z, x, tile_row, convention)
    lat_f = math.floor(south)
    lon_f = math.floor(west)
    lat_s = f"S{abs(lat_f)}" if lat_f < 0 else str(lat_f)
    lon_s = f"W{abs(lon_f)}" if lon_f < 0 else str(lon_f)
    return f"{lat_s}_{lon_s}"


def _read_source_metadata(source_path):
    """Read all metadata rows from a source MBTiles file."""
    conn = sqlite3.connect(f"file:{source_path}?mode=ro", uri=True)
    cur  = conn.cursor()
    cur.execute("SELECT name, value FROM metadata")
    meta = {row[0]: row[1] for row in cur.fetchall()}
    conn.close()
    return meta


def _build_grid_metadata(grid_key, source_meta, tile_coords, convention):
    """Build accurate metadata for one grid file from its actual tile contents."""
    meta = dict(source_meta)
    min_lon, min_lat =  180.0,  90.0
    max_lon, max_lat = -180.0, -90.0
    zoom_levels_seen = set()

    for z, x, tr in tile_coords:
        west, south, east, north = tile_bounds(z, x, tr, convention)
        min_lon = min(min_lon, west)
        min_lat = min(min_lat, south)
        max_lon = max(max_lon, east)
        max_lat = max(max_lat, north)
        zoom_levels_seen.add(z)

    center_lon  = (min_lon + max_lon) / 2
    center_lat  = (min_lat + max_lat) / 2
    center_zoom = max(zoom_levels_seen) if zoom_levels_seen else 16

    meta['name']   = f"India Navigator {grid_key}"
    meta['bounds'] = f"{min_lon:.6f},{min_lat:.6f},{max_lon:.6f},{max_lat:.6f}"
    meta['center'] = f"{center_lon:.6f},{center_lat:.6f},{center_zoom}"
    if zoom_levels_seen:
        meta['minzoom'] = str(min(zoom_levels_seen))
        meta['maxzoom'] = str(max(zoom_levels_seen))
    return meta


def _mbtiles_repack_worker(args):
    """
    Multiprocessing worker: reads all tiles from the shared source MBTiles,
    writes only the tiles belonging to its assigned grid keys.
    """
    worker_id, source_path, output_dir, grid_keys, commit_interval, \
        source_meta, convention = args
    output_dir = Path(output_dir)

    src     = sqlite3.connect(f"file:{source_path}?mode=ro", uri=True)
    grid_tile_coords = {gk: [] for gk in grid_keys}
    dbs     = {}

    def get_db(gk):
        if gk in dbs:
            return dbs[gk]
        path = output_dir / f"{gk}.mbtiles"
        conn = sqlite3.connect(str(path))
        cur  = conn.cursor()
        cur.executescript("""
            PRAGMA synchronous  = OFF;
            PRAGMA journal_mode = MEMORY;
            PRAGMA temp_store   = MEMORY;
            PRAGMA cache_size   = -65536;
            CREATE TABLE IF NOT EXISTS metadata (
                name  TEXT PRIMARY KEY,
                value TEXT
            );
            CREATE TABLE IF NOT EXISTS tiles (
                zoom_level  INTEGER,
                tile_column INTEGER,
                tile_row    INTEGER,
                tile_data   BLOB,
                PRIMARY KEY (zoom_level, tile_column, tile_row)
            );
        """)
        conn.commit()
        dbs[gk] = (conn, cur, 0)
        return dbs[gk]

    src_cur = src.cursor()
    src_cur.execute(
        "SELECT zoom_level, tile_column, tile_row, tile_data FROM tiles"
    )

    processed = 0
    skipped   = 0

    for z, x, tr, tile_data in src_cur:
        gk = tile_to_grid_key(z, x, tr, convention)
        if gk not in grid_keys:
            skipped += 1
            continue

        conn, cur, pending = get_db(gk)
        cur.execute(
            "INSERT OR REPLACE INTO tiles "
            "(zoom_level, tile_column, tile_row, tile_data) VALUES (?, ?, ?, ?)",
            (z, x, tr, tile_data)
        )
        grid_tile_coords[gk].append((z, x, tr))
        pending += 1
        dbs[gk]  = (conn, cur, pending)

        if pending >= commit_interval:
            conn.commit()
            dbs[gk] = (conn, cur, 0)

        processed += 1

    # Write per-grid metadata derived from actual tile contents
    for gk, (conn, cur, _pending) in dbs.items():
        meta = _build_grid_metadata(gk, source_meta, grid_tile_coords[gk], convention)
        cur.executemany(
            "INSERT OR REPLACE INTO metadata (name, value) VALUES (?, ?)",
            meta.items()
        )
        conn.commit()
        conn.close()

    src.close()
    return worker_id, processed, skipped


def repack_tiles_from_mbtiles(source_mbtiles, output_dir, commit_interval=2000):
    """
    Stream tiles from a single source MBTiles into per-1°-grid MBTiles files.

    This replaces the old osmium-extract-per-tile + parallel-tilemaker approach:
    - No intermediate PBF files are written
    - No inode pressure
    - Single SQLite scan, N parallel writers
    """
    source     = str(source_mbtiles)
    output_dir = Path(output_dir)

    # Validate source before doing anything
    if not _validate_mbtiles(source):
        sz = os.path.getsize(source) if os.path.exists(source) else -1
        raise ValueError(
            f"Source MBTiles is missing or corrupt (size={sz}): {source}. "
            f"Delete it and re-run tilemaker."
        )

    output_dir.mkdir(parents=True, exist_ok=True)

    # Wipe and recreate output directory for a fully clean start
    if output_dir.exists():
        log(f"Clearing output directory: {output_dir}")
        shutil.rmtree(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    os.chmod(output_dir, 0o755)

    log("Detecting tile_row convention...")
    convention  = detect_tile_row_convention(source)
    source_meta = _read_source_metadata(source)
    log("Source MBTiles metadata:")
    for k, v in source_meta.items():
        display = str(v) if len(str(v)) < 80 else str(v)[:77] + "..."
        log(f"  {k:20s} = {display}")

    # Index scan (no blobs) to discover grid cells and their tile counts
    log("Scanning tile index (no blobs)...")
    src = sqlite3.connect(f"file:{source}?mode=ro", uri=True)
    cur = src.cursor()
    cur.execute("SELECT COUNT(*) FROM tiles")
    total_tiles = cur.fetchone()[0]
    log(f"  Total tiles: {total_tiles:,}")

    cur.execute("SELECT DISTINCT zoom_level FROM tiles ORDER BY zoom_level")
    zoom_levels = [r[0] for r in cur.fetchall()]
    log(f"  Zoom levels: {zoom_levels}")

    cur.execute("SELECT zoom_level, tile_column, tile_row FROM tiles")
    grid_tile_count: dict = {}
    for z, x, tr in cur:
        gk = tile_to_grid_key(z, x, tr, convention)
        grid_tile_count[gk] = grid_tile_count.get(gk, 0) + 1
    src.close()

    all_grid_keys = sorted(grid_tile_count.keys())
    log(f"  Unique 1° grid cells: {len(all_grid_keys)}")

    bad_keys = [k for k in all_grid_keys if k.startswith('S') or k.startswith('W')]
    if bad_keys:
        log(f"  ⚠ {len(bad_keys)} keys outside normal India range: {bad_keys[:10]}", "WARN")
    else:
        try:
            lat_vals = [int(k.split('_')[0]) for k in all_grid_keys]
            lon_vals = [int(k.split('_')[1]) for k in all_grid_keys]
            log(f"  ✓ lat {min(lat_vals)}–{max(lat_vals)}, lon {min(lon_vals)}–{max(lon_vals)}")
        except Exception:
            pass

    # Distribute keys round-robin by load, heaviest keys first
    n_workers   = max(1, MAX_WORKERS)
    log(f"Writing with {n_workers} parallel workers...")
    keys_by_load = sorted(all_grid_keys, key=lambda k: -grid_tile_count[k])
    worker_keys  = [set() for _ in range(n_workers)]
    for i, k in enumerate(keys_by_load):
        worker_keys[i % n_workers].add(k)

    worker_args = [
        (wid, source, str(output_dir), keys, commit_interval, source_meta, convention)
        for wid, keys in enumerate(worker_keys)
    ]

    task_id = job_progress.add_task("[green]Repacking into grid MBTiles...", total=len(all_grid_keys))
    results = []
    try:
        with multiprocessing.Pool(processes=n_workers) as pool:
            done_grids = 0
            for result in pool.imap_unordered(_mbtiles_repack_worker, worker_args):
                wid, written, skipped = result
                log(f"  Worker {wid} done — {written:,} tiles written ({skipped:,} skipped)")
                results.append(result)
                done_grids += len(worker_keys[wid])
                job_progress.update(task_id, completed=done_grids)
    finally:
        job_progress.remove_task(task_id)

    total_written = sum(r[1] for r in results)
    log(f"✓ Wrote {total_written:,} tiles into {len(all_grid_keys)} grid files", "SUCCESS")

    log("Top 10 grid cells by tile count:")
    for gk, cnt in sorted(grid_tile_count.items(), key=lambda x: -x[1])[:10]:
        path    = output_dir / f"{gk}.mbtiles"
        size_kb = path.stat().st_size / 1024 if path.exists() else 0
        log(f"  {gk}.mbtiles — {cnt:,} tiles, {size_kb:,.0f} KB")

    return True


