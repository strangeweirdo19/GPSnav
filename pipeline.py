def is_in_md5_cooldown():
    """Check if we're still in the 12-hour cooldown period after an MD5 mismatch."""
    last_mismatch = get_last_md5_mismatch_timestamp()
    if not last_mismatch:
        return False
    
    try:
        last_time = datetime.fromisoformat(last_mismatch)
        time_since = datetime.now() - last_time
        cooldown_hours = MD5_MISMATCH_COOLDOWN_HOURS
        
        if time_since.total_seconds() < (cooldown_hours * 3600):
            remaining = (cooldown_hours * 3600) - time_since.total_seconds()
            hours_remaining = remaining / 3600
            log(f"⏳ MD5 mismatch cooldown active: {hours_remaining:.1f} hours remaining", "WARN")
            return True
        else:
            # Cooldown expired, remove the file
            try:
                os.remove(MD5_MISMATCH_FILE)
                log("✓ MD5 mismatch cooldown expired", "INFO")
            except OSError:
                pass
            return False
    except Exception as e:
        log(f"Error checking MD5 cooldown: {e}", "DEBUG")
        return False

