def get_patch_filename(file_type, patch_tier, date=None):
    """Generate standardized patch filename with proper timestamp.
    
    Args:
        file_type: 'india', 'poi', 'navigator'
        patch_tier: 'daily', 'weekly', 'monthly', 'quarterly', 'yearly'
        date: datetime object, defaults to now
    
    Returns:
        Filename with proper timestamp format
    """
    if date is None:
        date = datetime.now()
    
    # Different timestamp formats for different tiers
    if patch_tier == 'daily':
        timestamp = date.strftime('%Y-%m-%d')
    elif patch_tier == 'weekly':
        # Week number format: 2026-W06 (week 6 of 2026)
        week_num = date.isocalendar()[1]
        timestamp = f"{date.year}-W{week_num:02d}"
    elif patch_tier == 'monthly':
        timestamp = date.strftime('%Y-%m')
    elif patch_tier == 'quarterly':
        quarter = (date.month - 1) // 3 + 1
        timestamp = f"{date.year}-Q{quarter}"
    elif patch_tier == 'yearly':
        timestamp = str(date.year)
    else:
        timestamp = date.strftime('%Y-%m-%d')
    
    return f"patch_{file_type}_{patch_tier}_{timestamp}"

