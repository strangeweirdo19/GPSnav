def create_manifest_for_directory(directory, name="manifest"):
    """Create a manifest file listing all files with their MD5 hashes.
    
    For Valhalla tiles, excludes files that are exactly 272 bytes (corrupted/empty tiles).
    """
    manifest = {
        "name": name,
        "created": datetime.now().isoformat(),
        "files": {}
    }
    
    if not os.path.exists(directory):
        return None
    
    try:
        skipped_count = 0
        for root, dirs, files in os.walk(directory):
            for file in files:
                file_path = os.path.join(root, file)
                rel_path = os.path.relpath(file_path, directory)
                
                try:
                    file_size = os.path.getsize(file_path)
                    
                    # Skip Valhalla tiles that are exactly 272 bytes (corrupted/empty tiles)
                    if file.endswith('.gph') and file_size == 272:
                        log(f"Skipping corrupted Valhalla tile (272 bytes): {rel_path}", "DEBUG")
                        skipped_count += 1
                        continue
                    
                    file_md5 = calculate_file_md5(file_path)
                    
                    manifest["files"][rel_path] = {
                        "md5": file_md5,
                        "size_bytes": file_size,
                        "size_mb": round(file_size / (1024**2), 2)
                    }
                except Exception as e:
                    log(f"Failed to hash {file_path}: {e}", "DEBUG")
        
        if skipped_count > 0:
            log(f"Skipped {skipped_count} corrupted Valhalla tiles (272 bytes)", "INFO")
        
        return manifest
    except Exception as e:
        log(f"Failed to create manifest for {directory}: {e}", "ERROR")
        return None


def calculate_all_md5_in_directory(directory):
    """Calculate and save MD5 for all files in directory."""
    if not os.path.exists(directory):
        return 0
    
    count = 0
    try:
        for root, dirs, files in os.walk(directory):
            for file in files:
                file_path = os.path.join(root, file)
                
                # Skip if already .md5 file or manifest
                if file.endswith('.md5') or file == 'manifest.json':
                    continue
                
                try:
                    md5_hash = calculate_file_md5(file_path)
                    md5_file = file_path + ".md5"
                    
                    with open(md5_file, 'w') as f:
                        f.write(md5_hash)
                    
                    count += 1
                except Exception as e:
                    log(f"Failed to create MD5 for {file_path}: {e}", "DEBUG")
    except Exception as e:
        log(f"Error walking directory {directory}: {e}", "ERROR")
    
    return count

def safe_rmtree(path):
    """Safely remove directory tree, handling symlinks."""
    if not os.path.exists(path):
        return
    try:
        for root, dirs, files in os.walk(path, topdown=False):
            for f in files:
                try:
                    os.remove(os.path.join(root, f))
                except OSError:
                    pass
            for d in dirs:
                d_path = os.path.join(root, d)
                if os.path.islink(d_path):
                    try:
                        os.unlink(d_path)
                    except OSError:
                        pass
                else:
                    try:
                        os.rmdir(d_path)
                    except OSError:
                        pass
        os.rmdir(path)
    except Exception as e:
        log(f"Warning: Could not fully clean {path}: {e}", "WARN")

