def create_merged_patches():
    """Create weekly patches from daily and monthly patches from weekly."""
    if not os.path.exists(PATCH_DIR):
        return
        
    try:
        current_date = datetime.now()
        
        # Check if we should create weekly patches (every Monday or if we have 7+ incremental patches)
        should_create_weekly = current_date.weekday() == 0 or check_patch_count_threshold('incremental', 7)
        
        # Check if we should create monthly patches (1st of month or if we have 4+ weekly patches)
        should_create_monthly = current_date.day == 1 or check_patch_count_threshold('weekly', 4)

        # Check if we should create yearly patches (Jan 1st or if we have 13+ monthly patches)
        should_create_yearly = (current_date.month == 1 and current_date.day == 1) or check_patch_count_threshold('monthly', 13)
        
        if should_create_weekly:
            create_weekly_merged_patches(current_date)
            
        if should_create_monthly:
            create_monthly_merged_patches(current_date)

        if should_create_yearly:
            create_yearly_merged_patches(current_date)
            
    except Exception as e:
        log(f"Error creating merged patches: {e}", "WARN")

