def cleanup_old_patches():
    """Clean up old patches according to retention policies for all file types."""
    if not os.path.exists(PATCH_DIR):
        return
    
    try:
        current_date = datetime.now()
        
        # Get all patch files
        patch_files = [f for f in os.listdir(PATCH_DIR) if f.endswith('.lz4')]
        
        # Categorize patches by file type and schedule
        patches_by_type = {
            'india': {'incremental': [], 'weekly': [], 'monthly': [], 'yearly': []},
            'poi': {'incremental': [], 'weekly': [], 'monthly': [], 'yearly': []},
            'navigator': {'incremental': [], 'weekly': [], 'monthly': [], 'yearly': []}
        }
        
        for patch_file in patch_files:
            # Parse filename to extract file type and schedule
            if patch_file.startswith('patch_') and '.lz4' in patch_file:
                # Remove .lz4 extension first
                base_name = patch_file.replace('.lz4', '')
                parts = base_name.split('_')
                
                if len(parts) >= 4:
                    file_type = parts[1]  # india, poi, navigator
                    schedule = parts[2]    # incremental, weekly, monthly, yearly
                    
                    if file_type in patches_by_type and schedule in ['incremental', 'weekly', 'monthly', 'yearly']:
                        patches_by_type[file_type][schedule].append(patch_file)
        
        # Clean up each category
        total_removed = 0
        
        for file_type in patches_by_type:
            for schedule in patches_by_type[file_type]:
                patches = patches_by_type[file_type][schedule]
                patches.sort(reverse=True)  # Latest first
                
                if schedule == 'incremental':
                    # Keep incremental patches for 7 days (for weekly merging)
                    for patch in patches:
                        try:
                            # Extract date from filename: patch_type_incremental_YYYY-MM-DD.ext.lz4
                            base_name = patch.replace('.lz4', '')
                            parts = base_name.split('_')
                            if len(parts) >= 4:
                                date_str = parts[3]  # Should be YYYY-MM-DD
                                
                                try:
                                    patch_date = datetime.strptime(date_str, '%Y-%m-%d')
                                    days_old = (current_date - patch_date).days
                                    
                                    if days_old > DAILY_PATCH_RETENTION_DAYS:  # Now 7 days
                                        remove_patch_files(patch)
                                        total_removed += 1
                                except ValueError:
                                    log(f"Could not parse date from patch filename: {patch}", "DEBUG")
                        except Exception as e:
                            log(f"Error processing incremental patch {patch}: {e}", "DEBUG")
                            
                elif schedule == 'weekly':
                    # Keep only last 2 weekly patches
                    for patch in patches[WEEKLY_PATCH_RETENTION_COUNT:]:
                        remove_patch_files(patch)
                        total_removed += 1
                        
                elif schedule == 'monthly':
                    # Keep only last 13 monthly patches (364 days)
                    for patch in patches[MONTHLY_PATCH_RETENTION_COUNT:]:
                        remove_patch_files(patch)
                        total_removed += 1
                        
                elif schedule == 'yearly':
                    # Keep only last n yearly patches
                    for patch in patches[YEARLY_PATCH_RETENTION_COUNT:]:
                        remove_patch_files(patch)
                        total_removed += 1
        
        # Log cleanup summary
        remaining = len([f for f in os.listdir(PATCH_DIR) if f.endswith('.lz4')])
        if total_removed > 0:
            log(f"✓ Patch cleanup: removed {total_removed}, {remaining} patches retained", "SUCCESS")
        else:
            log(f"✓ Patch cleanup: {remaining} patches retained (no cleanup needed)", "DEBUG")
        
    except Exception as e:
        log(f"Error during patch cleanup: {e}", "WARN")

