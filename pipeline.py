        new_files = get_gph_files(new_dir)
        
        # Find changed/new files by comparing MD5s
        # Note: calculate_file_md5 automatically skips the 272-byte header for .gph files
        changed_files = []
        
        for rel_path, new_path in new_files.items():
            if rel_path not in old_files:
                # New tile file
                changed_files.append((rel_path, new_path))
                log(f"New Valhalla tile: {rel_path}", "DEBUG")
            else:
                # Compare content (skipping .gph header automatically)
                old_md5 = calculate_file_md5(old_files[rel_path])
                new_md5 = calculate_file_md5(new_path)
                if old_md5 != new_md5:
                    changed_files.append((rel_path, new_path))
                    log(f"Changed Valhalla tile: {rel_path}", "DEBUG")
        
        # Check for deleted tiles
        deleted_count = 0
        for rel_path in old_files:
            if rel_path not in new_files:
                deleted_count += 1
                log(f"Deleted Valhalla tile: {rel_path}", "DEBUG")
        
        if not changed_files and deleted_count == 0:
            log("No changes in Valhalla routing tiles", "INFO")
            return 0
        
