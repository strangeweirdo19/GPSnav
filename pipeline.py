def calculate_file_md5(filepath):
    """
    Calculate MD5 hash of a file.
    For Valhalla .gph tiles, skips the 272-byte header (which contains date_created,
    dataset_id, checksum that change daily) and only hashes the actual tile data.
    """
    hash_md5 = hashlib.md5()
    with open(filepath, "rb") as f:
        # For Valhalla .gph tiles, skip the 272-byte GraphTileHeader
        # This header contains metadata (date_created, dataset_id, checksum) that
        # changes daily even when the actual road network data is identical
        if filepath.endswith('.gph'):
            header = f.read(272)  # Skip GraphTileHeader (272 bytes)
            if len(header) < 272:
                # File too small, hash entire file
                f.seek(0)
        
        # Hash the remaining data (or entire file for non-.gph files)
        for chunk in iter(lambda: f.read(4096), b""): 
            hash_md5.update(chunk)
    
    return hash_md5.hexdigest()

def save_single_file_md5(filepath):
    md5_hash = calculate_file_md5(filepath)
    with open(filepath + ".md5", 'w') as f: f.write(md5_hash)
    return md5_hash

def download_file_resumable(url, dest_path):
    """Downloads file with Resume support."""
    session = requests.Session()
    retry = Retry(connect=5, backoff_factor=0.5)
    adapter = HTTPAdapter(max_retries=retry)
    session.mount('http://', adapter)
    session.mount('https://', adapter)

    existing_size = 0
    if os.path.exists(dest_path):
        existing_size = os.path.getsize(dest_path)

    headers = {}
    mode = 'wb'
    if existing_size > 0:
        headers['Range'] = f'bytes={existing_size}-'
        mode = 'ab' 
        log(f"Resuming partial download from {existing_size // 1024 // 1024} MB", "INFO")

    try:
        with session.get(url, headers=headers, stream=True) as r:
            if r.status_code == 416:
                log("Download complete", "SUCCESS")
                return True
            r.raise_for_status()
            
            if existing_size > 0 and r.status_code == 200:
                existing_size = 0
                mode = 'wb'

            total_length = r.headers.get('content-length')
            total_size = int(total_length) + existing_size if total_length else None

            task_id = job_progress.add_task("[cyan]Downloading...", total=total_size, completed=existing_size)
            
            try:
                with open(dest_path, mode) as f:
                    for data in r.iter_content(chunk_size=8192):
                        if shutdown_event.is_set():
                            return False
                        f.write(data)
                        job_progress.update(task_id, advance=len(data))
            finally:
                job_progress.remove_task(task_id)
                
        return True
    except Exception as e:
        log(f"Download error: {e}", "ERROR")
        return False

