        def get_gph_files(directory):
            """Get all .gph files with their relative paths."""
            gph_files = {}
            if os.path.exists(directory):
                for root, dirs, files in os.walk(directory):
                    for file in files:
                        if file.endswith('.gph'):
                            full_path = os.path.join(root, file)
                            rel_path = os.path.relpath(full_path, directory)
                            gph_files[rel_path] = full_path
            return gph_files
        
